package com.buscapecompany.avaliacao.viagemfundomar.model;

public class Ponto {
	
	private int eixoX;
	private int eixoY;
	private int eixoZ;
	
	public Ponto() {
		super();
		this.eixoX = 0;
		this.eixoY = 0;
		this.eixoZ = 0;
	}
	
	public int getEixoX() {
		return eixoX;
	}
	public void setEixoX(int eixoX) {
		this.eixoX = eixoX;
	}
	public int getEixoY() {
		return eixoY;
	}
	public void setEixoY(int eixoY) {
		this.eixoY = eixoY;
	}
	public int getEixoZ() {
		return eixoZ;
	}
	public void setEixoZ(int eixoZ) {
		this.eixoZ = eixoZ;
	}

	
}
